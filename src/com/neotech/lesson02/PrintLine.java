package com.neotech.lesson02;

public class PrintLine {
	public static void main(String[] args) {
		System.out.println("What's up ");
		// println()-> prints
		System.out.println("How are you??");
  System.out.print("1");		
		
	//print()-> prints the whole statement but it doesn't remove the cursor to the next line	
		
		
  System.out.print("1");		
  System.out.print("2");		
  System.out.print("3");
  
  System.out.println(4);
	
	// we can use \n to insert a new line
  System.out.println("Do you like java??\n yes, i do");
		
  System.out.print("\n");
  System.out.print("\n");
  System.out.print("\n");
  System.out.print("\n");
  System.out.print("\n");
  
  System.out.println("let's display the line");
  	
 
  // we can use multiple escape sequence in the same display statement
  System.out.println("\n\n\n\n");
		
		
		
		
	}

}
